package BASICS;

public class OPERATOR_6 {
    public static void main(String[] args) {
        // arithmetic operator +,-,/,*,%
        int a=7;
        int b=14;
        System.out.println(a%b);
       // assignment operator =,+=,-=,*=
       int a2=34;
       a2+=16;
        System.out.println(a2);
        // logical operator &&,||
        System.out.println((64>1 )&&(6>1));
        // comparision operator ==,>,<
        System.out.println(0==1);
        // bitwise operator
        System.out.println(2&3);
    }

}
